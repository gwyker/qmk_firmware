// This is called during key processing before the actual key event is handled.
// If the function returns true QMK will process the keycode as usual.
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case MAGICTAB:
      if (record->event.pressed) {
        if (get_mods() & MOD_MASK_GUI) {
          tap_code(KC_GRV);
        }
        else if (get_mods() & MOD_MASK_SHIFT) {
          unregister_mods(MOD_LSFT);
          tap_code(KC_ESC);
          register_mods(MOD_LSFT);
        }
        else if (get_mods() & MOD_MASK_CTRL) {
          layer_invert(_GAME);
        }
        else {
          if (!is_alt_tab_active) {
            is_alt_tab_active = true;
            register_code(KC_LALT);
          }
          alt_tab_timer = timer_read();
          register_code(KC_TAB);
        }
      } else {
        unregister_code(KC_TAB);
      }
      break;
  }
  return true;
}

// Called whenever the layer state changes.
// Used to change the LED color.
layer_state_t layer_state_set_user(layer_state_t state) {
    state = update_tri_layer_state(state, _LOWER, _RAISE, _POWER);
    switch (get_highest_layer(state)) {
        case _LOWER:
            rgblight_sethsv_noeeprom(HSV_GREEN);
            rgblight_sethsv_noeeprom(rgblight_get_hue(), rgblight_get_sat(), 100);
            break;
        case _RAISE:
            rgblight_sethsv_noeeprom(HSV_AZURE);
            rgblight_sethsv_noeeprom(rgblight_get_hue(), rgblight_get_sat(), 100);
            break;
        case _POWER:
            rgblight_sethsv_noeeprom(HSV_RED);
            rgblight_sethsv_noeeprom(rgblight_get_hue(), rgblight_get_sat(), 100);
            break;
        case _GAME:
            rgblight_sethsv_noeeprom(HSV_CYAN);
            rgblight_sethsv_noeeprom(rgblight_get_hue(), rgblight_get_sat(), 100);
            break;
        case _MOUSE:
            rgblight_sethsv_noeeprom(HSV_YELLOW);
            rgblight_sethsv_noeeprom(rgblight_get_hue(), rgblight_get_sat(), 100);
            break;
        case _EMOJI:
            rgblight_sethsv_noeeprom(HSV_ORANGE);
            rgblight_sethsv_noeeprom(rgblight_get_hue(), rgblight_get_sat(), 100);
            break;
        case _ARROW:
            rgblight_sethsv_noeeprom(HSV_YELLOW);
            rgblight_sethsv_noeeprom(rgblight_get_hue(), rgblight_get_sat(), 100);
            break;
        default: //  for any other layers, or the default layer
            rgblight_sethsv_noeeprom(HSV_PURPLE);
            rgblight_sethsv_noeeprom(rgblight_get_hue(), rgblight_get_sat(), 100);
            break;
    }
    return state;
}

// Encoder function.
void encoder_update_user(uint8_t index, bool counterclockwise) {
    switch(biton32(layer_state)) {
        case _RAISE:
            if (counterclockwise) {
                tap_code(KC_VOLD);
            } else {
                tap_code(KC_VOLU);
            }
            break;
        case _GAME:
            if (counterclockwise) {
                tap_code(KC_VOLD);
            } else {
                tap_code(KC_VOLU);
            }
            break;
        case _LOWER:
            if (counterclockwise) {
                register_code(KC_LSFT);
                register_code(KC_LCTL);
                tap_code(KC_TAB);
                unregister_code(KC_LSFT);
                unregister_code(KC_LCTL);
            } else {
                register_code(KC_LCTL);
                tap_code(KC_TAB);
                unregister_code(KC_LCTL);
            }
            break;
        default:
            if (counterclockwise) {
                tap_code(KC_WH_U);
            } else {
                tap_code(KC_WH_D);
            }
            break;
    }
}

// Called once after the keyboard boots up
void keyboard_post_init_user(void) {
  // Customise these values to desired behaviour
  debug_enable=true;
  debug_matrix=true;
  debug_keyboard=true;
  //debug_mouse=true;
  rgblight_sethsv_noeeprom(HSV_PURPLE);
  set_unicode_input_mode(UC_WINC);
}

// Called ten times a second. Can lower performance!
void matrix_scan_user(void) {
  if (is_alt_tab_active) {
    if (timer_elapsed(alt_tab_timer) > 1000) {
      unregister_code(KC_LALT);
      is_alt_tab_active = false;
    }
  }
}

